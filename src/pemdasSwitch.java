public class pemdasSwitch {
    public static void main(String[] args) {
        int pilih; //deklarasi
        pilih = 2; //assignment
        switch (pilih){
            case 0:
                System.out.println("pilih 0");
                break;
            case 1:
                System.out.println("pilih 1");
                break;
            case 2:
                System.out.println("pilih 2");
                break;
            case 3:
                System.out.println("pilih 3");
                break;
        }
    }
}
